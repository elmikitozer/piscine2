#include <unistd.h>
int	main(int argc, char **av)
{
	int i = 0;
	int j = 0;
	if (argc == 3)
	{
		while (av[1][i])
		{
			while (av[2][j++])
			{
				if (av[1][i] == av[2][j])
					i++;
			}
		}
		if (av[1][i] != 0)
			write (1, "\n", 1);
		else
		{
			write(1, av[1], i);
			write (1, "\n", 1);
		}
	}
}
